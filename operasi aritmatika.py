# Operasi Aritmatika

a = 10
b = 3


# Operasi tambah (+)
hasil = a + b
print(a, '+', b,  '  =', hasil)

# Operasi pengurangan (-)
hasil = a - b
print(a, '-', b,  '  =', hasil)

# Operasi pembagian (/)
hasil = a/b
print(a, '/', b,  '  =', hasil)

# Operasi pembagian (*)
hasil = a*b
print(a, '*', b,  '  =', hasil)

# Operasi sisa bagi (%)
hasil = a%b
print(a, '%', b,  '  =', hasil)

# Operasi perpangkatan (**)
hasil = a**b
print(a, '**', b, ' =', hasil)

hasil = a==b
print(a, '==', b, ' =', hasil)

hasil = a!=b
print(a, '!=', b, ' =', hasil)
 
hasil = a>b
print(a, '>', b, '  =', hasil)

hasil = a<b
print(a, '<', b, '  =', hasil)

hasil = a>=b
print(a, '>=', b, ' =', hasil)

hasil = a<=b
print(a, '<=', b, ' =', hasil)

