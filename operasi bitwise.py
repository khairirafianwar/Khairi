# operator bitwise

a = 6
b = 3

# bitwise AND (&)
c = a & b
print('nilai :', a)
print('nilai :', b)
print('nilai bitwise AND :', c)

# bitwise OR (|)
c = a | b
print('nilai :', a)
print('nilai :', b)
print('nilai bitwise OR :', c)


# bitwise XOR (^)
c = a ^ b
print('nilai :', a)
print('nilai :', b)
print('nilai bitwise XOR :', c)

# bitwise NOT (~)
c = ~a
c = ~b
print('nilai :', a)
print('nilai :', b)
print('nilai bitwise NOT :', c)

# shift Right (>>)
c = a >> 2
print('nilai :', a)
print('nilai :', b)
print('nilai shift Right :', c)

# shift Left (<<)
c = a << 2
print('nilai :', a)
print('nilai :', b)
print('nilai sift Left :', c)
